--The WHERE Statement
      The WHERE clause is used to extract only those records that fulfill a specified criterion.
      The syntax for the WHERE clause:
SELECT column_list 
FROM table_name
WHERE condition;

      In a table, to SELECT a specific record:
SELECT * FROM customers
WHERE ID = 7;

